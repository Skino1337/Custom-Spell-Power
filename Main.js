var g_Mult = 3.0;
var g_Spells = true, g_Items = true, g_Heroes = false, g_SpecText = "Spells & Items";

var replacedMap = [], replaceValue = [];
function onGetAbilityValue(ability, abilityName, field, values)
{
	var fullName = abilityName + "." + field;
	
	if (typeof replacedMap[fullName] != "undefined")
		return;
	
	replacedMap[fullName] = true;
		
	values = values.map(function(v)
	{
		if (v % 1 == 0)
			return v;
		else
			return v.toFixed(2);
	});
	var oldValues = values;
	
	printToAll("values: " + values);
	
	if (abilityName.indexOf("item_") != -1)
	{
		if (!g_Items)
			return;
	}
	else
	{
		if (!g_Spells)
			return;
	}
	
	for (var i in changeParam)
	{
		if (changeParam[i][0] == fullName)
		{
			var steps;
			var flags = changeParam[i][1];
			var increase = flags.indexOf("i") != -1;
			var decrease = flags.indexOf("d") != -1;
			var chance = flags.indexOf("c") != -1;
			var stun = flags.indexOf("s") != -1;
			var fix = flags.indexOf("f") != -1;
			
			if (chance)
			{
				values = chanceMult();
				replaceValue[fullName] = oldValues + " => " + values;
				return values;
			}
			
			if (fix)
			{
				values = stepMult();
				replaceValue[fullName] = oldValues + " => " + values;
				return values;
			}
			
			values = values.map(function(v) {return boostParam(v);});
						
			if (stun)
			{
				values = values.map(function(v) {return v * 0.75;});
				values = values.map(function(v) {return Math.min(v, 2 * g_Mult);});
				replaceValue[fullName] = oldValues + " => " + values;
				return values;
			}
		}
	}
	
	replaceValue[fullName] = oldValues + " => " + values;
	
	return values;
	
	function chanceMult()
	{
		var cur_chance, chance;
		return values.map(function(v)
		{
			cur_chance = chance = Math.sqrt(v * v) / 100;
			
			for (var i = 0; i < (g_Mult / 1) - 1; i++)
				cur_chance = (cur_chance + (1 - cur_chance) * chance);
			
			cur_chance *= 100;
			
			if (v < 0) cur_chance * -1;
			
			return Math.round(cur_chance);
		});
	}
	
	function stepMult()
	{
		var fv = 1488, pv, rv, av;
		return values.map(function(v)
		{
			if (fv == 1488)
			{
				rv = pv = v;
              	av = fv = boostParam(rv);
                return fv;
			}
			
			rv = v - pv;
			pv = v;
          	av += rv;
			
			return av;
		});
	}
	
	function boostParam(v)
	{
		if (increase)
			return v * g_Mult;
		
		if (decrease)
			return v / g_Mult;
		
		return v;
	}
}

function onUnitParsed(unit, keyvalues)
{
	var unitClassName = unit.getClassname();
	var modelPath = "type: string " + keyvalues["Model"];
	
	if (g_Spells)
	{
		for (var i in spellSummoned)
		{
			if (modelPath.indexOf(spellSummoned[i]) != -1)
			{
				keyvalues["ArmorPhysical"] *= g_Mult;
				keyvalues["AttackDamageMin"] *= g_Mult;
				keyvalues["AttackDamageMax"] *= g_Mult;
				keyvalues["AttackRate"] -= (0.20 * g_Mult);
				keyvalues["MovementSpeed"] += (20 * g_Mult);
				
				keyvalues["StatusHealth"] *= g_Mult;
				keyvalues["StatusHealthRegen"] *= g_Mult;
				keyvalues["StatusMana"] *= g_Mult;
				keyvalues["StatusManaRegen"] *= g_Mult;
				
				break;
			}
		}
	}
	
	if (g_Items)
	{
		for (var i in itemSummoned)
		{
			if (modelPath.indexOf(spellSummoned[i]) != -1)
			{
				keyvalues["ArmorPhysical"] *= g_Mult;
				keyvalues["AttackDamageMin"] *= g_Mult;
				keyvalues["AttackDamageMax"] *= g_Mult;
				keyvalues["AttackRate"] -= (0.20 * g_Mult);
				keyvalues["MovementSpeed"] += (20 * g_Mult);
				
				keyvalues["StatusHealth"] *= g_Mult;
				keyvalues["StatusHealthRegen"] *= g_Mult;
				keyvalues["StatusMana"] *= g_Mult;
				keyvalues["StatusManaRegen"] *= g_Mult;
				
				break;
			}
		}
	}
	
	if (g_Heroes)
	{
		if (dota.heroes.indexOf(unitClassName) != -1)
		{
			keyvalues["AttributeBaseStrength"] *= g_Mult;
			keyvalues["AttributeBaseAgility"] *= g_Mult;
			keyvalues["AttributeBaseIntelligence"] *= g_Mult;
			
			keyvalues["AttributeStrengthGain"] *= g_Mult;
			keyvalues["AttributeAgilityGain"] *= g_Mult;
			keyvalues["AttributeIntelligenceGain"] *= g_Mult;
		}
	}
}

function onEntityHurt(event)
{
	var attacked = game.getEntityByIndex(event.getInt("entindex_killed"));
	var attacker = game.getEntityByIndex(event.getInt("entindex_attacker"));
	var inflictor = game.getEntityByIndex(event.getInt("entindex_inflictor"));
	var damageType = event.getInt("damagebits");
	
	var inflictorName = inflictor.getClassname();
	
	for (var i in nonBoostDamageSpells)
	{
		if (nonBoostDamageSpells[i][0].indexOf(inflictorName) != -1)
		{
			var skillLevel = inflictor.netprops.m_iLevel;
			var dmg = nonBoostDamageSpells[i][skillLevel] * (g_Mult - 1);
			
			dota.applyDamage(attacker, attacked, inflictor, dmg, damageType);
			
			//printToAll("Added extra damage: " + dmg);
		}
	}
}

function abilityInfo(client, args)
{
	var hero = client.netprops.m_hAssignedHero;
	if (!hero)
		return;
		
	if (args.length != 1)
	{
		client.printToChat("Wrong format, use \"-ai #\".");
		return;
	}
	
	var slot = parseInt(args[0]);
	if (isNaN(slot))
	{
		client.printToChat("Wrong format, \"#\" should be a number.");
		return;
	}
	
	slot--;
	if (slot < 0 || slot > 3)
	{
		client.printToChat("Wrong format, use a value between 1 and 4.");
		return;
	}
	
	var ability = hero.netprops.m_hAbilities[slot];
	if (!ability)
		return;
		
	var abilityName = ability.getClassname();
	client.printToChat(abilityName + ":");
	
	var paramCount = 0;
	for (var i in replaceValue)
	{
		if (i.indexOf(abilityName) != -1)
		{
			client.printToChat(i.substr(i.indexOf(".") + 1) + " " + replaceValue[i]);
			paramCount++;
		}
	}
	
	client.printToChat(" ");
	
	if (paramCount == 0)
		client.printToChat("You need to use the ability of at least one time to get the information.");
	else
		client.printToChat("Think should be different? SAY! All info in plugin page.");
}

function itemInfo(client, args)
{
	var hero = client.netprops.m_hAssignedHero;
	if (!hero)
		return;
		
	if (args.length != 1)
	{
		client.printToChat("Wrong format, use \"-ii #\".");
		return;
	}
	
	var slot = Number(args[0]);
	if (isNaN(slot))
	{
		client.printToChat("Wrong format, \"#\" should be a number.");
		return;
	}
	
	slot--;
	if (slot < 0 || slot > 5)
	{
		client.printToChat("Wrong format, use a value between 1 and 6.");
		return;
	}
	
	var item = hero.netprops.m_hItems[slot];
	if (!item)
	{
		client.printToChat("In this slot nothing.");
		return;
	}
		
	var itemName = item.getClassname();
	client.printToChat(itemName + ":");
	
	client.printToChat(" ");
		
	for (var i in replaceValue)
	{
		if (i.indexOf(itemName) != -1)
			client.printToChat(i.substr(i.indexOf(".") + 1) + " " + replaceValue[i]);
	}
}

